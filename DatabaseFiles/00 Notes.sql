Recipe 2: Meatball Bites
cuisine: American
1 package puff pastry Sheets
1 pound ground beef
3 tablespoons meatball Spice
2 tablespoons chopped parsley
1 egg 



In a bowl, mix the ground beef with seasoning and parsley. Mix in egg.
Roll them into bite-sized meatballs. Place them in the fridge for about 10 minutes.
In the meantime, lay out your puff pastry sheet and cut one-to-two-inch strips. 
Begin taking your meatballs and wrapping the puff pastry around. Slide them onto skewers. 
Repeat the process until you used up all of the puff pastry.
Coat with egg wash. Then, place in the oven to bake for about 25 to 30 minutes.

Recipe 3: Red Potato Salad
2 to 3 pounds small red potatoes
1  tablespoon mustard
1/2 cup Mayonnaise
2 to 3 tablespoons pickle juice
1 small purple onion
6 scallions
2 sour pickles
2 to 3 sprigs fresh dill, chopped

Boil red potatoes whole and unpeeled until cooked through. Let cool.
